#include "3-calc.h"
/**
 * op_add - sums a and b
 * @a: first int
 * @b: second int
 * Return: returns sum of a and b
 */
int op_add(int a, int b)
{
	return ((a) + (b));
}
/**
 * op_sub - subs a and b
 * @a: first int
 * @b: second int
 * Return: returns difference of a and b
 */
int op_sub(int a, int b)
{
	return ((a) - (b));
}
/**
 * op_mul - multiplies a and b
 * @a: first int
 * @b: second int
 * Return: returns product of a and b
 */
int op_mul(int a, int b)
{
	return ((a) * (b));
}
/**
 * op_div - divides a and b
 * @a: first int
 * @b: second int
 * Return: returns result of division of a and b
 */
int op_div(int a, int b)
{
	return ((a) / (b));
}
/**
 * op_mod - mod of division of a and b
 * @a: first int
 * @b: second int
 * Return: returns remainder of division of a and b
 */
int op_mod(int a, int b)
{
	return ((a) % (b));
}
